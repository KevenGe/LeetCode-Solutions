# 869. 重新排序得到 2 的幂
#

################################################################################


class Solution:
    def reorderedPowerOf2(self, n: int) -> bool:

        targets = set()
        for i in range(32):
            targets.add("".join(sorted(str(2 ** i))))

        # targets = {
