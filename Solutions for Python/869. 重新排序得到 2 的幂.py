        #     "35566",
        #     "012356789",
        #     "1234446788",
        #     "23678",
        #     "8",
        #     "0124",
        #     "13468",
        #     "0469",
        #     "234455668",
        #     "122446",
        #     "1289",
        #     "46",
        #     "011237",
        #     "0368888",
        #     "112234778",
        #     "0248",
        #     "0145678",
        #     "125",
        #     "0134449",
        #     "0122579",
        #     "224588",
        # }

        tmp = "".join(sorted(str(n)))
        return tmp in targets


